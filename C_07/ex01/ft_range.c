#include <stdlib.h>
#include <stdio.h>

int	*ft_range(int min, int max)
{
	int	*range;
	int	i;

	i = 0;
	if (min >= max)
		return (NULL);
	range = (int *)malloc((max - min) * sizeof(int));
	if (range == NULL)
		return (NULL);
	while (min < max)
		range[i++] = min++;
	return (range);
}
